package de.fraunhofer.scai.bio.uima.resourcefileparsers;

/**
 * This package contains 4 parsers required to generate various Hashmaps and a multimap required to
 * normalize the miRNA terms found.
 * 
 * Gff3ToHashmap.java converts hsa.gff3 file into Hashmap
 * Gff3ToMultimap.java converts hsa.gff3 file into Guava Multimap
 * MirnaAliasToHashmap.java converts alias.txt file into Hashmap
 * MiRNAdead2Hashmap.java converts miRNA.dead file into Hashmap
 * MiFam2Hashmap.java converts miFam.dat file into Hashmap
 * readingEMBLFormat converts miRNA.dat file into map file and description file (This class is currently removed)
 * 
 * The links to find these files can be found in docs folder of the project MiRNADetector.
 * 
 * @author Anjani K Dhrangadhariya
 */